-- Data Manipulation Language (DML)
-- Explanation: DML commands (INSERT, UPDATE, DELETE) add, modify, or remove rows within a table.
-- When to Use: Use DML to manage the data itself, such as adding new records, updating existing ones, or removing outdated data.

-- Example:
INSERT INTO employees (name, age, department_id)
VALUES ('John Doe', 28, 1);

-- Output: Adds a new employee record to the employees table.
-- Practical Guide: Use DML for day-to-day data management, such as entering new orders or correcting errors in existing records.